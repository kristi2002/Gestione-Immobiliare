-- ── 1. Contract titles ───────────────────────────────────────
UPDATE contracts SET title = CONCAT('Locazione ', YEAR(start_date), ' - Unità #', id)
WHERE (title IS NULL OR title = '' OR title = 'Contratto locazione demo') AND contract_type = 'locazione';
UPDATE contracts SET title = CONCAT('Compravendita #', id)
WHERE (title IS NULL OR title = '' OR title = 'Contratto locazione demo') AND contract_type != 'locazione';

-- ── 2. Link commissions to contracts ────────────────────────
UPDATE agent_commissions SET contract_id = id WHERE contract_id IS NULL AND id <= (SELECT MAX(id) FROM contracts);

-- ── 3. Link insurance policies to clients via property owner ─
UPDATE property_insurance pi
JOIN properties p ON p.id = pi.property_id
SET pi.client_id = p.client_id
WHERE pi.client_id IS NULL AND p.client_id IS NOT NULL;

-- ── 4. Reminders: priority & request_type ───────────────────
UPDATE reminders SET priority = ELT(1 + (id MOD 4), 'urgente','alta','normale','bassa')
WHERE priority IS NULL OR priority = '';
UPDATE reminders SET request_type = ELT(1 + (id MOD 6),
    'Guasto idraulico','Problema elettrico','Riparazione infissi',
    'Manutenzione caldaia','Infiltrazioni','Altro')
WHERE request_type IS NULL OR request_type = '';
UPDATE reminders SET category = ELT(1 + (id MOD 5),
    'Urgente','Ordinaria','Straordinaria','Preventiva','Strutturale')
WHERE category IS NULL OR category = '';

-- ── 5. Reminders: spread maintenance statuses ────────────────
UPDATE reminders SET maintenance_status = 'aperta'         WHERE (id MOD 4) = 0;
UPDATE reminders SET maintenance_status = 'in_lavorazione' WHERE (id MOD 4) = 1;
UPDATE reminders SET maintenance_status = 'completata'     WHERE (id MOD 4) = 2;
UPDATE reminders SET maintenance_status = 'chiusa'         WHERE (id MOD 4) = 3;

-- ── 6. Reminders: populate tenant_name from tenants JOIN ─────
UPDATE reminders r
JOIN tenants t ON t.property_id = r.property_id AND t.status = 'active'
SET r.tenant_name = CONCAT(t.name, ' ', t.surname)
WHERE r.property_id IS NOT NULL AND (r.tenant_name IS NULL OR r.tenant_name = '');

-- ── 7. Reminders: assign suppliers ──────────────────────────
UPDATE reminders r
JOIN suppliers s ON s.id = (1 + (r.id MOD (SELECT COUNT(*) FROM suppliers)))
SET r.supplier_id = s.id, r.supplier_name = s.name
WHERE r.id MOD 2 = 0 AND r.supplier_id IS NULL;

