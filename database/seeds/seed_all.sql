-- ── 9. Meter readings: ensure each meter has 2 readings ──────
-- Insert a historical reading (30 days ago) for meters that only have 1
INSERT INTO meter_readings (property_id, meter_type, reading_value, reading_date, notes)
SELECT mr.property_id, mr.meter_type,
       GREATEST(0, mr.reading_value - ELT(1 + (mr.id MOD 5), 80, 120, 200, 150, 95)),
       DATE_SUB(mr.reading_date, INTERVAL 30 DAY),
       'Lettura precedente (seed)'
FROM meter_readings mr
WHERE NOT EXISTS (
    SELECT 1 FROM meter_readings prev
    WHERE prev.property_id = mr.property_id
      AND prev.meter_type  = mr.meter_type
      AND prev.id != mr.id
);

-- ── 10. Property applications: status spread ─────────────────
UPDATE property_applications SET status = ELT(1 + (id MOD 4), 'new','contacted','approved','rejected')
WHERE status IS NULL OR status = '';

-- ── 11. Surveys: ensure some surveys exist ───────────────────
INSERT IGNORE INTO tenant_surveys (tenant_id, property_id, token)
SELECT t.id, t.property_id, MD5(CONCAT('token_', t.id, '_survey'))
FROM tenants t
WHERE t.id <= 10
  AND NOT EXISTS (SELECT 1 FROM tenant_surveys ts WHERE ts.tenant_id = t.id);

